#include <stdio.h>
int main(void)
{
	int l = 0, m = 0, roadRemain = 0;
	scanf("%d %d", &l, &m);
	short int road[l];
	for (int t = 0; t < l; t++)
	{
		road[t] = 1;
	}
	for (int t = 0; t < m; t++)
	{
		int roadBegin, roadEnd;
		scanf("%d %d", &roadBegin, &roadEnd);
		for (int t = roadBegin; t < roadEnd + 1; t++)
		// 啊啊啊注意你的思路！！！你的t已经是开始的坐标了，判断的是结束位置，不是路段长啊！！！
		{
			road[t] = 0;
		}
	}
	for (int t = 0; t < l; t++)
	{
		if (road[t] == 1)
		{
			roadRemain++;
		}
	}
	printf("There are %d Trees remained!\n", roadRemain);
	return 0;
}